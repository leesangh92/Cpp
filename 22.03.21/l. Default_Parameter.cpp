#include <iostream>

using namespace std;

int BoxVolume(int length = 1, int width = 1, int height = 1); // 디폴트 인자는 선언부와 실행부가 나누어져 있다면 선언부에만 명시한다!!!

int main()
{
	cout << "BoxVolume(3, 3, 3) : " << BoxVolume(3, 3, 3) << endl;
	cout << "BoxVolume(5, 5) : " << BoxVolume(5, 5) << endl;
	cout << "BoxVolume(7) : " << BoxVolume(7) << endl;
	cout << "BoxVolume() : " << BoxVolume() << endl;

	return 0;
}

// 직육면체 부피 구하기
int BoxVolume(int length, int width, int height) // 디폴트 인자(디폴트 매개변수) = 1
{
	return length * width * height;
}
