#include <iostream>

using namespace std;

class Circle
{
private:
	int radius;
public:
	Circle()
	{
		radius = 1;
	}

	Circle(int r)
	{
		radius = r;
	}
	
	~Circle()
	{
		cout << "~Circle()" << endl;
	}

	int getRadius()
	{
		return radius;
	}

	double getArea()
	{
		return radius * radius * 3.14;
	}
};

int main()
{
	Circle donut;
	Circle pizza(30);

	Circle* p = &donut;
	cout << "p->getArea() : " << p->getArea() << endl;

	p = &pizza;
	cout << "p->getArea() : " << p->getArea() << endl;
	
	// 소멸자는 '객체'(donut과 pizza, p는 임시 변수)를 소멸시킨다. -> 소멸자는 2회 생성

	return 0;
}